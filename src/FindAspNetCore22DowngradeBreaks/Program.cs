internal static class Program
{
    private static int Main(string[] args)
    {
        args = [
            @"D:\\Code\\aspnet22-users\\",
            @"D:\\Code\\aspnet22-users\\aspnet22-users.csv"
        ];

        if (args.Length != 2)
        {
            var exeName = Path.GetFileNameWithoutExtension(Environment.ProcessPath);
            Console.Error.WriteLine($"usage: {exeName} <path-to-app-directory> <csv-output-path>");
            return 1;
        }

        var directoryPath = args[0];
        var outputPath = args[1];
        Run(directoryPath, outputPath);
        return 0;
    }

    private static void Run(string directoryPath, string outputPath)
    {
        Console.WriteLine("Loading breaking changes...");
        var data = ApiDatabase.Load();

        Console.WriteLine("Finding assemblies...");
        var files = Directory.GetFiles(directoryPath, "*", SearchOption.AllDirectories);
        Console.WriteLine($"Found {files.Length:N0} assembly file(s).");
        Console.WriteLine($"Looking for API usages...");

        using var writer = new CsvWriter(outputPath);
        writer.Write("Calling Assembly");
        writer.Write("GUID");
        writer.Write("Namespace");
        writer.Write("Type");
        writer.Write("Member");
        writer.WriteLine();

        foreach (var file in files)
        {
            var fileName = Path.GetFileName(file);
            if (_platformAssemblyNames.Contains(fileName))
                continue;

            var extension = Path.GetExtension(file);
            var isApplicationOrLibrary = string.Equals(".exe", extension, StringComparison.OrdinalIgnoreCase) ||
                                         string.Equals(".dll", extension, StringComparison.OrdinalIgnoreCase);
            if (!isApplicationOrLibrary)
                continue;

            using var env = new HostEnvironment();
            var assembly = env.LoadAssemblyFrom(file);
            if (assembly is null)
                continue;

            var crawler = new AssemblyCrawler();
            crawler.Crawl(assembly);
            var results = crawler.GetResults();

            foreach (var apiKey in results.Data.Keys)
            {
                var guid = apiKey.Guid;
                var apiEntry = data.GetEntry(guid);
                if (apiEntry is null)
                    continue;

                writer.Write(assembly.Name.Value);
                writer.Write(apiEntry.Guid.ToString("N"));
                writer.Write(apiEntry.NamespaceName);
                writer.Write(apiEntry.TypeName);
                writer.Write(apiEntry.MemberName);
                writer.WriteLine();
            }
        }
    }

